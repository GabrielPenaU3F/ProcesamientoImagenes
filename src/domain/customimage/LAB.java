package domain.customimage;

public class LAB {

    private final double L;
    private final double a;
    private final double b;

    public LAB(double L, double a, double b) {
        this.L = L;
        this.a = a;
        this.b = b;
    }

    public double getL() {
        return L;
    }

    public double getA() {
        return a;
    }

    public double getB() {
        return b;
    }
}
