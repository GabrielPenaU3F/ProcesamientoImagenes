package domain.customimage;

public class XYZ {

    private final double x;
    private final double y;
    private final double z;

    public XYZ(double X, double Y, double Z) {
        x = X;
        y = Y;
        z = Z;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }

    public double getZ() {
        return z;
    }
}
